    private void selectItem(){
        DefaultTableModel dtm=(DefaultTableModel) tblItem.getModel();
        int SelectRow=tblItem.getSelectedRow();
        txtItemCode.setText(dtm.getValueAt(SelectRow, 0).toString());
        txtDescription.setText(dtm.getValueAt(SelectRow, 1).toString());
        txtPrice.setText(dtm.getValueAt(SelectRow, 2).toString());
    }

    private void viewAllItem() {
        try {
            ArrayList<GoodsDTO>goodList=ctrlGoods.getAll();
            DefaultTableModel dtm=(DefaultTableModel) tblItem.getModel();
            dtm.setRowCount(0);
            for (GoodsDTO gList : goodList) {
                Object[]rowData={gList.getGid(),gList.getGood_name(),gList.getPrice()};
                dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    private void addItemAction(){
        DecimalFormat df=new DecimalFormat("#,###.00");
        df.setMaximumFractionDigits(2);
        DecimalFormat dfd=new DecimalFormat("#");
        dfd.setMaximumFractionDigits(2);
        
        String code=txtItemCode.getText();
        String description=txtDescription.getText();
        int unitprice=Integer.parseInt(txtPrice.getText());
        
        
        GoodsDTO goods=new GoodsDTO(code, description, unitprice);
        try {
            boolean isAdded=ctrlGoods.add(goods);
            if(isAdded){
                JOptionPane.showMessageDialog(this,"Item has been successfully added...!");
                viewAllItem();
                clearTxt();
            }else{
                JOptionPane.showMessageDialog(this,"Item has not been added...!");
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
     private void genarateItemID() {
        
        try {
            String id=ctrlID.getNewID("goods", "gid", "G", 3);
            txtItemCode.setText(id);
        } catch (Exception ex) {
            Logger.getLogger(GinPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void clearTxt(){
        txtDescription.setText("");
        txtPrice.setText("");
        txtPrice.setText("");
        genarateItemID();
    }
