    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        DecimalFormat df=new DecimalFormat("#,###.00");
        df.setMaximumFractionDigits(2);
        DecimalFormat dfd=new DecimalFormat("#");
        dfd.setMaximumFractionDigits(2);
        
        String code=txtItemCode.getText();
        String description=txtDescription.getText();
        int unitprice=Integer.parseInt(txtPrice.getText());
       
        
        GoodsDTO goods=new GoodsDTO(code, description, unitprice);
        try {
            boolean isUpdated=ctrlGoods.update(goods);
            //System.out.println(ctrlGoods.update(goods));
            if(isUpdated){
                JOptionPane.showMessageDialog(this,"Item has been successfully updated...!");
                viewAllItem();
                clearTxt();
            }else{
                JOptionPane.showMessageDialog(this,"Item has not been updated...!");
            }
        } catch (Exception ex) {
            Logger.getLogger(ItemPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

