package com.kencode.project.dto;

/**
 *
 * @author Vidura
 */
public class OrdersDTO extends SuperDTO{
    
    private String oid;
    private int amount;
    private String date;

    public OrdersDTO() {
    }

    public OrdersDTO(String oid, int amount, String date) {
        this.oid = oid;
        this.amount = amount;
        this.date = date;
    }

    /**
     * @return the oid
     */
    public String getOid() {
        return oid;
    }

    /**
     * @param oid the oid to set
     */
    public void setOid(String oid) {
        this.oid = oid;
    }

    /**
     * @return the amount
     */
    public int getAmount() {
        return amount;
    }

    /**
     * @param amount the amount to set
     */
    public void setAmount(int amount) {
        this.amount = amount;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    
}
